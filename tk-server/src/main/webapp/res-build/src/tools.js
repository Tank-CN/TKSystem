define(function (require, exports, module) {
    var pageTool = {
        startPageLoading: function (message) {
            $('.page-loading').remove();
            $('body').append('<div class="page-loading"><span class="iconfont iconfont-animate">&#xe627;</span> <span class="load-text">' + (message ? message : '页面载入中...') + '</span></div>');
        },

        stopPageLoading: function () {
            $('.page-loading').remove();
        }
    };
    return pageTool;
});